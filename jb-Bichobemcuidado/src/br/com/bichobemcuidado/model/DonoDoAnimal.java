package br.com.bichobemcuidado.model;

public class DonoDoAnimal {
	public String nome;
	public int cpf;
	public int telefone;
	public String rua;
	public int numeroRua;
	public int numeroComplemento;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getCpf() {
		return cpf;
	}
	public void setCpf(int cpf) {
		this.cpf = cpf;
	}
	public int getTelefone() {
		return telefone;
	}
	public void setTelefone(int telefone) {
		this.telefone = telefone;
	}
	public String getRua() {
		return rua;
	}
	public void setRua(String rua) {
		this.rua = rua;
	}
	public int getNumeroRua() {
		return numeroRua;
	}
	public void setNumeroRua(int numeroRua) {
		this.numeroRua = numeroRua;
	}
	public int getNumeroComplemento() {
		return numeroComplemento;
	}
	public void setNumeroComplemento(int numeroComplemento) {
		this.numeroComplemento = numeroComplemento;
	}
	
	
	

}
